package model;

public class PobbangVO {
	private String name;
	private int price;
	private int cnt;
	private int num; // pk
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	@Override
	public String toString() {
		return "PobbangVO [name=" + name + ", price=" + price + ", cnt=" + cnt + "]";
	}
}